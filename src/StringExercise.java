public class StringExercise {
    public static void main(String[] args) {

        String variable = "We don't need no education\n" +
                "We don't need no thought control";
        System.out.println(variable);

        String variable2 = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(variable2);

        String variable3 = "In windows, the main drive is usually C:\\";
        System.out.println(variable3);

        String variable4 = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(variable4);

    }
}
